<h3><u>Young Fellow</u></h3>
<p>I once met le young fellow of the moor. T'was a great lad, I say. His lucious eyes stared into my soul, but trying to remain mentally intact, I escaped the act.</p>
<p>I cried that night. I remembered the time I met the nice lad. I reminded my self of the exact words he said in his soothing voice, "I'm gonna make you feel real good." It's amazing to feel that such great minds come up with such great philosophical statements. His intense yet enticing speech led me to pursuit the lad.</p>
<p>I worked through the night as I reached my former lollygagging habitat. Alas, the creature of interest was not there, for it was midnight. "He must be snozzin' in da Inn.", I thught to myself, as I pondered on whether the men would let me in. Yet I never met such an ardent fellow hitherto so I set out to pursue his flowing bald head.</p> 
<p>I entered the inn and spoke fervent "Alas. I am in the search for le young lad I met of the moor this day! If thy glowing eyes perceive me, do come out, good fellow!" Yet the silence of solitude surrounded my presence, for it was empty. A sense of forboding crawled up my left knee as I realised that this was, in fact, not an inn, but a deep, dark cave. Shocked by this discovery, I stared around the area until I saw the glowing eyes again. "Hah! I lay my eyes upon thee as thou lay thy eyes upon me!" I exclaimed with ardour. Yet the figure that emerged shocked me. It was him, the young fellow I met of the moor. I watched as he crawled out on his 8 legs from the shadows. His bald head, his intense glowing eyes, and his soothing voice, that's all I need. Alas, I was surprised as the rest of the figure arised. As I lay my eyes upon it, I perceived that it was infact being ridden by a skellington! How could the young fellow do such a thing to me? I wanted to get to know him but I never expected this! Shocked by the image of the "Spider Jockey" position, I ran out in tears. I will never forget that night as it still haunts me in my nightmares.</p>
<p>The End.</p>
